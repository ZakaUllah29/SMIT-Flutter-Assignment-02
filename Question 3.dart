// Q.3: Create a list of Days and remove one by one from the end of list.
void main() {
  List<String> Days = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday"];
  print(Days);
  Days.remove("Monday");
  print(Days);
  Days.remove("Tuesday");
  print(Days);
  Days.remove("Wednesday");
  print(Days);
  Days.remove("Thursday");
  print(Days);
  Days.remove("Friday");
  print(Days);
}
